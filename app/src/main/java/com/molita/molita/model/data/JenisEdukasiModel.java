package com.molita.molita.model.data;

public class JenisEdukasiModel {
    private String id_jenis_edukasi;
    private String nama_edukasi;
    private String slug;

    public String getId_jenis_edukasi() {
        return id_jenis_edukasi;
    }

    public void setId_jenis_edukasi(String id_jenis_edukasi) {
        this.id_jenis_edukasi = id_jenis_edukasi;
    }

    public String getNama_edukasi() {
        return nama_edukasi;
    }

    public void setNama_edukasi(String nama_edukasi) {
        this.nama_edukasi = nama_edukasi;
    }

    public String getSlug() {
        return slug;
    }

    public void setSlug(String slug) {
        this.slug = slug;
    }
}
